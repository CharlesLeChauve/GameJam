#include "solong.h"

int ft_cookiz(t_data *data)
{
    int i;
    int j;
    int c;

    i = 0;
    j = 0;
    c = 0;
    while (data->map[i])
    {
        while(data->map[i][j])
        {
            if (data->map[i][j] == 'C')
                c++;
            j++;
        }
        j = 0;
        i++;
    }
    return(c);
}